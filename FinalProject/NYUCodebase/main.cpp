		position[1] += velocity[1] * elapsed;
		collisionY();
		velocity[0] = lerp(velocity[0], 0.0f, elapsed * FRICTION_X);
		velocity[0] += GRAVITY_X * elapsed;
		velocity[0] += acceleration[0] * elapsed;
